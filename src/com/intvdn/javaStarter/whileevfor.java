package com.intvdn.javaStarter;

public class whileevfor {
        public static void main(String[] args) {
            int x = 1;

            while (x < 11) {
                System.out.print(x++ + " ");
            }
            System.out.println();

            for (int a = 1; a < 11; a++) {
                System.out.print(a + " ");
            }
        }
}
